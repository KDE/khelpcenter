void
KHelpView::setDefaultFontBase(int fSize)
{
  resetFontSizes();
  if (fSize != 3)
  {
    int fontSizes[7];
    getFontSizes(fontSizes);

    if (fSize > 3)
    {
      for(int i = 0; i < 7; i++)
      {
        int j = i + fSize - 3;
        if ( j > 6)
           j = 6;
    	fontSizes[i] = fontSizes[j]; 
      }
    }
    else
    {
      for(int i = 7; i--;)
      {
        int j = i + fSize - 3;
        if ( j < 0)
           j = 0;
    	fontSizes[i] = fontSizes[j]; 
      }
    }
    setFontSizes(fontSizes);
  }
}

bool KHelpView::URLVisited( const char *_url )
{
    return urlHistory->inHistory( _url );
}

